// Converts the verified comics-catalog.json into src/lib/comics-data.ts
// (the generated catalogue module). Titles are normalized to
// "Series N" display names regardless of how messy the archive item
// title is.
//
// Run: node scripts/emit-comics-ts.mjs

import { readFileSync, writeFileSync } from "node:fs";

const raw = JSON.parse(readFileSync(new URL("./comics-catalog.json", import.meta.url), "utf8"));

const genreOrder = ["scifi", "hero", "horror", "jungle", "war", "crime"];
raw.sort((a, b) =>
  genreOrder.indexOf(a.genre) - genreOrder.indexOf(b.genre) ||
  a.series.localeCompare(b.series) ||
  (a.issue ?? 0) - (b.issue ?? 0),
);

const seen = new Set();
const lines = [];
for (const c of raw) {
  if (seen.has(c.id)) continue;
  seen.add(c.id);
  const shortSeries = c.series
    .replace(/, Queen of the Jungle/, "")
    .replace(/ Comics$/, (m) => (c.series.split(" ").length > 2 ? "" : m));
  const title = c.issue ? `${shortSeries} ${c.issue}` : shortSeries;
  lines.push(
    `  { id: ${JSON.stringify(c.id)}, title: ${JSON.stringify(title)}, series: ${JSON.stringify(shortSeries)}, year: ${JSON.stringify(c.year || "")}, pages: ${c.pages}, genre: ${JSON.stringify(c.genre)}, pdf: ${JSON.stringify(c.pdf ?? null)} },`,
  );
}

const body = `// GENERATED by scripts/emit-comics-ts.mjs from the verified output of
// scripts/build-comics-catalog.mjs. Do not hand-edit; re-run the
// builder + emitter to refresh. ${lines.length} issues.

import type { ComicIssue } from "./comics";

export const COMICS_DATA: ComicIssue[] = [
${lines.join("\n")}
];
`;

writeFileSync(new URL("../src/lib/comics-data.ts", import.meta.url), body);
console.log(`wrote src/lib/comics-data.ts with ${lines.length} issues`);
