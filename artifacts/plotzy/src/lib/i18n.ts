export type TranslationKey = keyof typeof translations.en;

export function getT(lang: Language) {
  return (key: string): string => {
    const dict = translations[lang] as Record<string, string> | undefined;
    if (dict?.[key]) return dict[key];
    const en = translations.en as Record<string, string>;
    if (en[key]) return en[key];
    return key;
  };
}

export function detectBrowserLanguage(): Language {
  // A previously chosen language always wins. The in-app language
  // switcher writes "sw-lang" to localStorage, so a user who picked
  // Arabic keeps Arabic on every future visit from that browser.
  const saved = localStorage.getItem("sw-lang");
  if (saved && SUPPORTED_UI_LANGS.includes(saved as Language)) {
    return saved as Language;
  }
  // Every new visitor starts in English regardless of their browser
  // or OS language. Arabic and the other locales stay available, but
  // only by an explicit switch (which then persists above). This is
  // intentional: English is the universal default while the rest of
  // the localization work lands.
  return "en";
}
