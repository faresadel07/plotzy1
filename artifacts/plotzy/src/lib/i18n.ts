export type TranslationKey = keyof typeof translations.en;

export function getT(lang: Language) {
  return (key: string): string => {
    const dict = translations[lang] as Record<string, string> | undefined;
    if (dict?.[key]) return dict[key];
    const en = translations.en as Record<string, string>;
    if (en[key]) return en[key];
    return key;
  };
}

export function detectBrowserLanguage(): Language {
  const saved = localStorage.getItem("sw-lang");
  if (saved && SUPPORTED_UI_LANGS.includes(saved as Language)) {
    return saved as Language;
  }
  const browserLangs = navigator.languages || [navigator.language];
  for (const bl of browserLangs) {
    const base = bl.split("-")[0].toLowerCase() as Language;
    if (SUPPORTED_UI_LANGS.includes(base)) return base;
  }
  return "en";
}
